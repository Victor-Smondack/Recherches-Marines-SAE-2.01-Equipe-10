package src.metier;

public class Zones
{
    private int coordX;
    private int coordY;

    public Zones(int coordX, int coordY)
    {
        this.coordX = coordX;
        this.coordY = coordY;
    }

    public int getCoordX() { return this.coordX; }
    public int getCoordY() { return this.coordY; }    
}